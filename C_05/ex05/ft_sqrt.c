int	ft_sqrt(int nb)
{
	long long	i;

	i = 0;
	if (nb <= 0)
		return (0);
	while (i * i < (long long)nb)
		i++;
	if (i * i == (long long)nb)
		return (i);
	else
		return (0);
}
